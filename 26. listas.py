# Las listas son estructuras de datos mas flexibles, en las cuales puedo almacenar diferentes valores.
# Valores de tipo entero, decimal, caractes, cadena de caracteres, variables, valpres booleanos, otras listas, etc.

# Listas1 -Que es y como mostrar sus valores-
lista = []
print(lista)

# Listas2 -Ejemplo de mostrar todos los elementos de la lista-
lista = ["Lunes", "Martes", "Miercoles", "Jueves", "Viernes"]
print(lista)

# Listas3 -Mostramos el elemento segun la posicion-
lista = ["Lunes", "Martes", "Miercoles", "Jueves", "Viernes"]
print(lista[0])

# Listas4 -Representar el ultimo elemento y mostrarlo al reves-
lista = ["Lunes", "Martes", "Miercoles", "Jueves", "Viernes"]
print(lista[-1]) # Representa el ultimo elemento de la lista

# Listas5 -Mostrar el rango de elementos que quiero mostrar-
lista = ["Lunes", "Martes", "Miercoles", "Jueves", "Viernes"]
print(lista[0:3]) #Ejemplo1
print(lista[:3]) #Ejemplo2
print(lista[1:4]) #Ejemplo3